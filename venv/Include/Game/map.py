class Map:
    def __init__(self, game):
        self.game = game
        self.mini_map = mini_map
        self.world_map = {}
        self.rows = len(self.mini_map)
        self.cols = len(self.mini_map[0])
        self.get_map()

    def get_map(self):
        for j, row in enumerate(self.mini_map):
            for i, value in enumerate(row):
                if value:
                    self.world_map[(i, j)] = value

    def draw(self):
        [pg.draw.rect(self.game.screen, 'darkgray', (pos[0] * 100, pos[1] * 100, 100, 100), 2)
         for pos in self.world_map]


    def find_shortest_distance(start):
        end = (27, 12)
        map = mini_map
        row_count = len(map)
        col_count = len(map[0])
        visited = [[False for _ in range(col_count)] for _ in range(row_count)]
        queue = deque([(start[0], start[1], 0)])
        while queue:
            i, j, distance = queue.popleft()
            if i == end[0] and j == end[1]:
                return distance
            if i < 0 or i >= row_count or j < 0 or j >= col_count:
                continue
            if visited[i][j] or map[i][j] in (1, 2, 3, 4, 5):
                continue
            visited[i][j] = True
            queue.append((i - 1, j, distance + 1))
            queue.append((i + 1, j, distance + 1))
            queue.append((i, j - 1, distance + 1))
            queue.append((i, j + 1, distance + 1))